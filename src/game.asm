
	INCLUDE	SHIKE2.INC
	INCLUDE	KBD.INC

	INCLUDE	GEOMETRY.INC
	INCLUDE MOVABLE.INC

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	CSEG
	PUBLIC	GAME
	EXTRN	CLRSPR,LEVELINIT,MOBINIT,MOVINIT,NEWFRAME
	EXTRN	DISSCR,MOB.OFF,NEWMOV,SETCAMERA,GAMELAYERS
	EXTRN	PLACE,STEP,ANIMATE,KEY2DIR

GAME:   CALL	DISSCR
	CALL	CLRSPR
	CALL	GAMELAYERS
	CALL	MOB.OFF
	CALL	LEVELINIT
	CALL	MOBINIT
	CALL	MOVINIT

	LD	IX,MOV1
	LD	E,0
	LD	C,3
	CALL	NEWMOV
	LD	DE,POINT1
	LD	C,0
	CALL	PLACE

	LD	IX,MOV2
	LD	E,1
	LD	C,3
	CALL	NEWMOV
	LD	DE,POINT2
	LD	C,0
	CALL	PLACE

	LD	DE,MOV1
	CALL	SETCAMERA

G.LOOP: CALL	NEWFRAME
	CALL	ANIMATE
	CALL	GETKEY
	CP	KB_ESC
	RET	Z

	CALL	KEY2DIR
	LD	IX,MOV1
	JR	C,G.LOOP
	CALL	STEP
	JR	G.LOOP
	RET


POINT1:	DB	0		;LEVEL=0
	DW	0101H		;ROOM=0
	DB	15,0,0		;Y=X=Z=0

POINT2:	DB	0		;LEVEL=0
	DW	0202H		;ROOM=0
	DB	0,0,0		;Y=15,X=Z=0

	DSEG
MOV1:	DS	MOV.SIZ		;TODO: TEMPORAL BUFFER
MOV2:	DS	MOV.SIZ		;TODO: TEMPORAL BUFFER

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	CSEG
	EXTRN	GAMELAYERS,CLRSPR,EDLEVEL,KBHIT,RESETCAMERA,FUNKEYS

GETKEY:	CALL	KBHIT
	RET	Z

	CP	KB_SELECT
	JR	NZ,G.FUN
	CALL	EDLEVEL
	CALL	CLRSPR
	CALL	GAMELAYERS
	JR	G.RES

G.FUN:	CALL	FUNKEYS
	RET	Z

G.RES:	PUSH	AF
	LD	IX,MOV1
	CALL	RESETCAMERA
	POP	AF
	RET

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	CSEG
	PUBLIC	ENGINE

ENGINE:	RET

