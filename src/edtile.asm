
	INCLUDE	BIOS.INC
	INCLUDE	SHIKE2.INC
	INCLUDE	EVENT.INC
	INCLUDE	LEVEL.INC

NR_TILES	EQU	19

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	CSEG
	PUBLIC	ED.TILE
	EXTRN	CLRVPAGE,EDINIT,VDPSYNC,LISTEN

ED.TILE:CALL	EDINIT
	LD	E,EDPAGE
	CALL	CLRVPAGE
	CALL	SHOWSCR
	CALL	VDPSYNC

	LD	DE,RECEIVERS
	CALL	LISTEN
	RET

RECEIVERS:
	DB	1,253,0,254
	DW	TILEEVENT
	DB	0


;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

	CSEG
	PUBLIC	SHOWSCR

SHOWSCR:LD	C,0
	LD	DE,TILEYSIZ*8

S.LOOP:	PUSH	BC
	PUSH	DE
	CALL	DRAWTILE
	POP	DE
	LD	A,D
	ADD	A,TILEXSIZ*16
	JR	NZ,S.1
	LD	A,E
	ADD	A,TILEYSIZ*8
	LD	E,A
	XOR	A
S.1:	LD	D,A
	POP	BC
	LD	A,C
	INC	A
	LD	C,A
	CP	NR_TILES
	JR	NZ,S.LOOP
	RET

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;INPUT:	DE = SCREEN COORDENATES
;	C = TILE NUMBER

	CSEG
	EXTRN	VDPPAGE,PNUM2XY,LMMM,MULTDEA,TILEDEF

DRAWTILE:
	LD	A,LOGTIMP
	LD	(LOGOP),A
	LD	A,PATPAGE
	LD	(VDPPAGE),A

	PUSH	DE
	LD	A,C
	LD	DE,TILE.SIZ
	CALL	MULTDEA
	LD	DE,TILEDEF
	ADD	HL,DE
	LD	(DT.PTR),HL
	POP	DE

	LD	B,TILEXSIZ
T.LOOPX:PUSH	BC			;LOOP OVER X
	PUSH	DE

	LD	B,TILEYSIZ
T.LOOPY:PUSH	BC			;LOOP OVER Y
	PUSH	DE

	PUSH	DE
	LD	HL,(DT.PTR)		;TAKE THE PATTERN NUMBER
	LD	E,(HL)
	INC	HL
	LD	(DT.PTR),HL
	CALL	PNUM2XY			;CONVERT IT TO COORDENATES
	POP	DE
	LD	BC,1008H
	CALL	LMMM			;COPY
	POP	DE
	LD	A,E
	SUB	8
	LD	E,A
	POP	BC
	DJNZ	T.LOOPY

	POP	DE
	LD	A,D
	ADD	A,16
	LD	D,A
	POP	BC
	DJNZ	T.LOOPX
	RET

	DSEG
DT.PTR:	DW	0


;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;INPUT:	A = EVENT
;	DE = SCREEN LOCATION

	CSEG
	EXTRN	EDTILE

TILEEVENT:
	CP	MS_BUTTON1
	RET	NZ
	LD	A,E
	SRL	A
	SRL	A
	SRL	A
	LD	E,0

T.LOOP:	SUB	TILEYSIZ
	JR	C,T.BIG
	INC	E
	JR	T.LOOP

T.BIG:	LD	A,E
	RLCA
	RLCA
	RLCA
	LD	E,A
	LD	A,D
	AND	0E0H
	RRCA
	RRCA
	RRCA
	RRCA
	RRCA
	ADD	A,E
	CP	NR_TILES
	RET	NC
	INC	A
	LD	(EDTILE),A
	RET



