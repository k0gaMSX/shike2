	CSEG
	PUBLIC	EDHEIGTH
	EXTRN	EDINIT,NEWHEIGTH,GRID

EDHEIGTH:
	CALL	EDINIT
	LD	DE,HEIGTHBUF
	CALL	NEWHEIGTH

	LD	A,TILPAGE
	LD	(ACPAGE),A
	CALL	GRID			;SHOW ISOMETRIC GRID


	RET

	DSEG
HEIGTHBUF:	DS	HEIGTHSIZ

